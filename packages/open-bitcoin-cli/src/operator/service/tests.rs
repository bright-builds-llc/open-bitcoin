static NEXT_TEST_DIRECTORY_ID: AtomicU64 = AtomicU64::new(0);

struct TestDirectory {
    path: PathBuf,
}

impl TestDirectory {
    fn new(label: &str) -> Self {
        let directory = std::env::temp_dir().join(format!(
            "open-bitcoin-service-tests-{label}-{}",
            NEXT_TEST_DIRECTORY_ID.fetch_add(1, Ordering::Relaxed)
        ));
        std::fs::create_dir_all(&directory).expect("test directory");
        Self { path: directory }
    }
}

impl Drop for TestDirectory {
    fn drop(&mut self) {
        let _ = std::fs::remove_dir_all(&self.path);
    }
}

#[test]
fn plist_content_contains_required_fields() {
    // Arrange
    let binary_path = Path::new("/fake/bin/open-bitcoin");
    let data_dir = Path::new("/fake/datadir");

    // Act
    let plist = generate_plist_content("org.open-bitcoin.node", binary_path, data_dir, None, None);

    // Assert
    assert!(
        plist.contains("<string>org.open-bitcoin.node</string>"),
        "plist must contain label"
    );
    assert!(
        plist.contains("<string>/fake/bin/open-bitcoin</string>"),
        "plist must contain binary path"
    );
    assert!(
        plist.contains("<string>/fake/datadir</string>"),
        "plist must contain data dir"
    );
    assert!(
        plist.contains("<true/>"),
        "plist must contain true (KeepAlive/RunAtLoad)"
    );
    assert!(
        plist.contains("Generated by Open Bitcoin"),
        "plist must contain generator comment"
    );
}

