//! Tests for service lifecycle generators, FakeServiceManager, and dry-run safety.

use std::{
    path::{Path, PathBuf},
    sync::atomic::{AtomicU64, Ordering},
};

use crate::operator::service::{
    ServiceError, ServiceInstallRequest, ServiceLifecycleState, ServiceManager,
    ServiceStateSnapshot,
    fake::{FakeServiceCall, FakeServiceManager},
    launchd::{LaunchdAdapter, generate_plist_content},
    systemd::{SystemdAdapter, generate_unit_content},
};

static NEXT_TEST_DIRECTORY_ID: AtomicU64 = AtomicU64::new(0);

/// RAII temp directory for isolation in service tests.
struct TestDirectory {
    path: PathBuf,
}

impl TestDirectory {
    fn new(label: &str) -> Self {
        let directory = std::env::temp_dir().join(format!(
            "open-bitcoin-service-tests-{label}-{}",
            NEXT_TEST_DIRECTORY_ID.fetch_add(1, Ordering::Relaxed)
        ));
        std::fs::create_dir_all(&directory).expect("test directory");
        Self { path: directory }
    }
}

impl Drop for TestDirectory {
    fn drop(&mut self) {
        let _ = std::fs::remove_dir_all(&self.path);
    }
}

// --- Plist generator tests ---

#[test]
fn plist_content_contains_required_fields() {
    // Arrange
    let binary = Path::new("/fake/bin/open-bitcoin");
    let datadir = Path::new("/fake/datadir");

    // Act
    let content = generate_plist_content("org.open-bitcoin.node", binary, datadir, None, None);

    // Assert
    assert!(
        content.contains("<string>org.open-bitcoin.node</string>"),
        "missing label: {content}"
    );
    assert!(
        content.contains("<string>/fake/bin/open-bitcoin</string>"),
        "missing binary path: {content}"
    );
    assert!(
        content.contains("<string>/fake/datadir</string>"),
        "missing datadir: {content}"
    );
    assert!(content.contains("<true/>"), "missing KeepAlive or RunAtLoad: {content}");
    assert!(
        content.contains("Generated by Open Bitcoin"),
        "missing comment header: {content}"
    );
}

