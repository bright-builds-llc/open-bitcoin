#[test]
fn launchd_install_dry_run_does_not_write_file() {
    // Arrange
    let test_dir = TestDirectory::new("launchd-dryrun");
    let adapter = LaunchdAdapter::new(test_dir.path.clone());
    let request = ServiceInstallRequest {
        binary_path: PathBuf::from("/fake/bin/open-bitcoin"),
        data_dir: PathBuf::from("/fake/datadir"),
        maybe_config_path: None,
        maybe_log_path: None,
        apply: false,
    };

    // Act
    let result = adapter.install(&request);

    // Assert
    assert!(result.is_ok(), "dry-run install must succeed");
    let outcome = result.expect("install outcome");
    assert!(outcome.dry_run, "apply=false must produce dry_run=true");

    let plist_path = test_dir
        .path
        .join("Library")
        .join("LaunchAgents")
        .join("org.open-bitcoin.node.plist");
    assert!(
        !plist_path.exists(),
        "dry-run install must not write plist file"
    );
}

