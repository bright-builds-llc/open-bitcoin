// --- LaunchdAdapter dry-run isolation tests ---

#[test]
fn launchd_install_dry_run_does_not_write_file() {
    // Arrange
    let test_dir = TestDirectory::new("launchd-dry-run");
    let home_dir = test_dir.path.clone();
    let adapter = LaunchdAdapter::new(home_dir.clone());
    let request = ServiceInstallRequest {
        binary_path: PathBuf::from("/fake/bin/open-bitcoin"),
        data_dir: PathBuf::from("/fake/datadir"),
        maybe_config_path: None,
        maybe_log_path: None,
        apply: false,
    };

    // Act
    let result = adapter.install(&request);

    // Assert
    assert!(result.is_ok(), "dry-run should succeed: {result:?}");
    let outcome = result.unwrap();
    assert!(outcome.dry_run, "outcome should be dry_run=true");
    let plist_path = home_dir
        .join("Library")
        .join("LaunchAgents")
        .join("org.open-bitcoin.node.plist");
    assert!(
        !plist_path.exists(),
        "plist file must NOT exist after dry-run"
    );
}

