// --- FakeServiceManager tests ---

#[test]
fn fake_manager_install_records_call() {
    // Arrange
    let manager = FakeServiceManager::unmanaged();
    let request = ServiceInstallRequest {
        binary_path: PathBuf::from("/fake/bin/open-bitcoin"),
        data_dir: PathBuf::from("/fake/datadir"),
        maybe_config_path: None,
        maybe_log_path: None,
        apply: true,
    };

    // Act
    let result = manager.install(&request);

    // Assert
    assert!(result.is_ok(), "install should succeed: {result:?}");
    let calls = manager.recorded_calls.borrow();
    assert_eq!(calls.len(), 1);
    assert_eq!(calls[0], FakeServiceCall::Install { apply: true });
}

#[test]
fn fake_manager_status_returns_configured_state() {
    // Arrange
    let snapshot = ServiceStateSnapshot {
        state: ServiceLifecycleState::Unmanaged,
        maybe_service_file_path: None,
        maybe_manager_diagnostics: None,
        maybe_log_path: None,
    };
    let manager = FakeServiceManager::new(snapshot.clone());

    // Act
    let result = manager.status();

    // Assert
    assert!(result.is_ok());
    assert_eq!(result.unwrap(), snapshot);
    let calls = manager.recorded_calls.borrow();
    assert_eq!(calls.len(), 1);
    assert_eq!(calls[0], FakeServiceCall::Status);
}

